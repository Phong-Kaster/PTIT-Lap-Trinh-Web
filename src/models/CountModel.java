package models;

public class CountModel {
	private long post;
	private long cat;
	private long cmt;
	private long sub;
	private long user;
	
	
	public long getUser() {
		return user;
	}
	public void setUser(long user) {
		this.user = user;
	}
	public long getPost() {
		return post;
	}
	public void setPost(long post) {
		this.post = post;
	}
	public long getCat() {
		return cat;
	}
	public void setCat(long cat) {
		this.cat = cat;
	}
	public long getCmt() {
		return cmt;
	}
	public void setCmt(long cmt) {
		this.cmt = cmt;
	}
	public long getSub() {
		return sub;
	}
	public void setSub(long sub) {
		this.sub = sub;
	}
	

	
	
	
	
}
